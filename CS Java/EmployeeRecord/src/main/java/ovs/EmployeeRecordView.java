    private void AddActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_AddActionPerformed
        employees c;
        String employeeID, first_name, last_name, annualSalary, start_date; // define variables

        // Store textbox values into the variables
        employeeID = employeeIn.getText();
        first_name = firstIn.getText();
        last_name = lastIn.getText();
        annualSalary = salaryIn.getText();
        start_date = dateIn.getText();
        
        if (employeeID.isEmpty()){ // checks to see if employeeID is empty
            JOptionPane.showMessageDialog(null, "Error, please enter valid employeeID."); // prints error if employeeID is empty
        } else {
            try {
                int employeeIDInt = Integer.parseInt(employeeID);

                c = new employees(employeeID, first_name, last_name, annualSalary, start_date); 
                records.add(c);
        
                // Clear textboxes after adding to inventory
                employeeIn.setText("");
                firstIn.setText("");
                lastIn.setText("");
                salaryIn.setText("");
                dateIn.setText(""); 
            } catch(NumberFormatException e) { // if employeeIDInt isn't a integer catch here as number format exception and show messaged dialog of error
                JOptionPane.showMessageDialog(null, "Error, please enter a valid number for ID#.");
            }
        }
    }//GEN-LAST:event_AddActionPerformed

    private void ListActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_ListActionPerformed
        String temp="";

        for (int x=0; x<=records.size()-1; x++) {
            temp = temp + records.get(x).employeeID + " "
                    + records.get(x).first_name + " "
                    + records.get(x).last_name + " "
                    + records.get(x).annualSalary + " "
                    + records.get(x).start_date + "\n"; // displays the info of the array list as strings
        }
        inventoryOut.setText(temp);
    }//GEN-LAST:event_ListActionPerformed

