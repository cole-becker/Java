    private void RemoveActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_RemoveActionPerformed
        // TODO add your handling code here:
        String employeeRemove = employeeIn.getText();
        boolean found = false; // create boolean variable for finding number in list
        
        for (int x = 0; x < records.size(); x++){
            if (records.get(x).employeeID.equals(employeeRemove)){
                records.remove(x);
                JOptionPane.showMessageDialog(null, "Employee ID #" + employeeIn.getText() + " was succesfully removed."); // shows popup of correct employee id that was removed
                found = true; // if the employee id # is found

                // Clear textboxes after removing to applicants
                employeeIn.setText("");
                firstIn.setText("");
                lastIn.setText("");
                salaryIn.setText("");
                dateIn.setText("");
                break;
            }
        }
        if (!found){ // if the id is not found
            JOptionPane.showMessageDialog(null, "Error, employee ID #" + employeeIn.getText() + " was not found in records."); // show message dialog saying error
        }
    }//GEN-LAST:event_RemoveActionPerformed

