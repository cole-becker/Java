    private void btnChangePositiveNegativeActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnChangePositiveNegativeActionPerformed
        // TODO add your handling code here:
        String currentText = txtDisplay.getText();
        if (!currentText.isEmpty() && !currentText.equals("0")) { // Checks for whether or not the current display is empty or 0
            double number = Double.parseDouble(currentText);
            number = -number; // Reverses the sign from positive to negative
            txtDisplay.setText(Double.toString(number));
        }
    }//GEN-LAST:event_btnChangePositiveNegativeActionPerformed

