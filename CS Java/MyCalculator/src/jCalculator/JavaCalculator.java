    private void btnEqualsActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnEqualsActionPerformed
        // TODO add your handling code here:
        double number2 = Double.parseDouble(txtDisplay.getText()); // Convert last number entered and solution to double
        double solution = 0;

        int operation = (int) operation1; // Convert operation1 to integer

        switch (operation) { // Create switch and cases for all the types of calculations
        case 1: // Operation1 == 1 : Addition
            solution = total1 + number2;
            break;
        case 2: // Operation1 == 2 : Subtraction
            solution = total1 - number2;
            break;
        case 3: // Operation1 == 3 : Multipilcation
            solution = total1 * number2;
            break;
        case 4: // Operation1 == 4 : Division
            if (number2 == 0) {
                txtDisplay.setText("Error, cannot divide by 0."); // display error, Can't divide by 0
                return;
            } else {
                solution = total1 / number2;
            }
            break;
        case 5: // Operation1 == 5 : Power
            solution = Math.pow(total1, number2);
            break;
        default:
            break;
    }

    txtDisplay.setText(Double.toString(solution)); // Displays solution
    total1 = 0;
    }//GEN-LAST:event_btnEqualsActionPerformed

